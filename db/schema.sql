-- 1. THE USERS: Stores the identity and their "Golden Baseline"
CREATE TABLE UserProfiles (
    profile_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Unique ID for each user profile
    user_hash VARCHAR2(64) NOT NULL,                                -- Salted hash of UserID for privacy
    device_type VARCHAR2(20),                                       -- Context: 'mobile' or 'desktop'
    avg_dwell NUMBER(10,4) DEFAULT 0.0,                             -- Baseline: Avg time key is held down
    avg_flight NUMBER(10,4) DEFAULT 0.0,                            -- Baseline: Avg time between keys
    session_count NUMBER DEFAULT 0,                                 -- Count of successful logins for drift calc
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP                -- Last time the 'Golden Baseline' changed
);

